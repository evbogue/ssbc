#! /usr/bin/env node
'use strict'

// Generates docs/api-reference.md from the static manifests in
// lib/builtin-plugins.js plus the hand-written prose in docs/api-notes.json.
//
//   node scripts/gen-api-reference.js          # write docs/api-reference.md
//   node scripts/gen-api-reference.js --check   # exit 1 if the file is stale
//
// The rendering is exported as `renderApiReference()` so the drift guard in
// test/api-reference.js can byte-compare a fresh render against the committed
// file without shelling out.

const fs   = require('fs')
const path = require('path')
const registry = require('../lib/builtin-plugins')

const REPO_ROOT    = path.join(__dirname, '..')
const NOTES_PATH   = path.join(REPO_ROOT, 'docs', 'api-notes.json')
const OUTPUT_PATH  = path.join(REPO_ROOT, 'docs', 'api-reference.md')

// The secret-stack core namespaces that exist on a live server but have no
// static manifest in this repository. Named here only so the reference can be
// explicit about what it deliberately leaves out.
const NON_MANIFEST_CORE = [
  'auth', 'address', 'manifest', 'multiserver', 'multiserverNet'
]

function loadNotes() {
  try {
    return JSON.parse(fs.readFileSync(NOTES_PATH, 'utf8'))
  } catch (err) {
    if (err.code === 'ENOENT') return {}
    throw err
  }
}

// Fully-qualified method name as used in api-notes.json and the CLI:
// bare for the root database (`publish`), namespaced for plugins (`gossip.add`).
function qualify(namespace, method) {
  return namespace ? namespace + '.' + method : method
}

function renderMethod(lines, namespace, method, type, note, isStub) {
  const qualified = qualify(namespace, method)
  lines.push('### `' + qualified + '` — `' + type + '`')
  lines.push('')

  const prefix = isStub ? '**Stub / no-op in this build.** ' : ''
  const summary = note && note.summary ? note.summary : ''
  if (summary) lines.push(prefix + summary)
  else if (prefix) lines.push(prefix.trim())
  else lines.push('_Not yet documented._')
  lines.push('')

  if (note && note.args && Object.keys(note.args).length) {
    lines.push('**Arguments:**')
    lines.push('')
    for (const argName of Object.keys(note.args)) {
      lines.push('- `' + argName + '` — ' + note.args[argName])
    }
    lines.push('')
  }

  if (note && note.example) {
    lines.push('**Example:**')
    lines.push('')
    lines.push('```')
    lines.push(note.example)
    lines.push('```')
    lines.push('')
  }
}

function renderApiReference() {
  const notes = loadNotes()
  const lines = []

  lines.push('<!-- Generated by scripts/gen-api-reference.js — do not edit by hand. -->')
  lines.push('<!-- Run `npm run gen:api-reference` to regenerate. -->')
  lines.push('')
  lines.push('# Built-in API reference')
  lines.push('')
  lines.push('This page lists every method in the **static RPC manifests** of the server\'s')
  lines.push('built-in plugins. It is generated from the shared plugin registry in')
  lines.push('`lib/builtin-plugins.js`, so it stays in lock-step with what the server actually')
  lines.push('mounts at startup. Hand-written summaries live in `docs/api-notes.json`.')
  lines.push('')
  lines.push('## Coverage and boundaries')
  lines.push('')
  lines.push('**Included:** the root database (`lib/db`) and every RPC-bearing built-in plugin —')
  lines.push('the complete set of methods this server exposes from its own static manifests.')
  lines.push('')
  lines.push('**Excluded by design:**')
  lines.push('')
  lines.push('- Dynamically installed user plugins (loaded by `ssb-plugins` at runtime).')
  lines.push('- Non-manifest secret-stack core methods such as ' +
    NON_MANIFEST_CORE.map((n) => '`' + n + '`').join(', ') + ', which the')
  lines.push('  framework adds to a live server but does not declare in a repository manifest.')
  lines.push('')
  lines.push('Methods that do not yet have prose are shown as `_Not yet documented._`. The')
  lines.push('coverage count in the footer tracks documentation progress.')
  lines.push('')

  let total = 0
  let documented = 0

  for (const entry of registry.referenceEntries()) {
    const manifest = entry.module && entry.module.manifest
    if (!manifest || Object.keys(manifest).length === 0) continue
    const isStub = entry.kind === 'stub'

    if (entry.namespace === null) {
      lines.push('## Root database (`' + entry.name + '`)')
      lines.push('')
      lines.push('Methods on the server\'s root object. These are called without a namespace')
      lines.push('prefix (e.g. `publish`, `whoami`).')
    } else {
      lines.push('## `' + entry.namespace + '`' + (isStub ? ' — stub' : ''))
      lines.push('')
      lines.push('Provided by the `' + entry.name + '` plugin.' +
        (isStub ? ' This is a compatibility stub; its methods are present for' +
          ' protocol compatibility but do little or no work in this build.' : ''))
    }
    lines.push('')

    for (const method of Object.keys(manifest)) {
      const type = manifest[method]
      const qualified = qualify(entry.namespace, method)
      const note = notes[qualified]
      if (note && note.summary) documented++
      total++
      renderMethod(lines, entry.namespace, method, type, note, isStub)
    }
  }

  lines.push('---')
  lines.push('')
  lines.push('_Coverage: ' + documented + ' of ' + total +
    ' built-in static manifest methods documented._')
  lines.push('')

  return lines.join('\n')
}

function main() {
  const content = renderApiReference()
  const check = process.argv.includes('--check')

  if (check) {
    let existing = null
    try { existing = fs.readFileSync(OUTPUT_PATH, 'utf8') } catch (_) {}
    if (existing === content) {
      console.log('docs/api-reference.md is up to date.')
      process.exit(0)
    }
    console.error('docs/api-reference.md is stale. Run `npm run gen:api-reference`.')
    process.exit(1)
  }

  fs.writeFileSync(OUTPUT_PATH, content)
  console.log('Wrote ' + path.relative(REPO_ROOT, OUTPUT_PATH))
}

module.exports = { renderApiReference, OUTPUT_PATH, NOTES_PATH }

if (require.main === module) main()
